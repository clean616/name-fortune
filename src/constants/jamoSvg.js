// 겹받침은 두 자음을 나란히 배치
const makePairPaths = (leftPaths, rightPaths, split = 50) => {
  const scale = split / 100;
  const left = leftPaths.map(d => scalePath(d, 0, scale, 0, 1));
  const offset = split;
  const rightScale = (100 - split) / 100;
  const right = rightPaths.map(d => scalePath(d, offset, rightScale, 0, 1));
  return [...left, ...right];
};

function scalePath(d, tx, sx, ty, sy) {
  return d.replace(/(-?\d+\.?\d*),(-?\d+\.?\d*)/g, (_, x, y) => {
    return `${(parseFloat(x) * sx + tx).toFixed(1)},${(parseFloat(y) * sy + ty).toFixed(1)}`;
  });
}

